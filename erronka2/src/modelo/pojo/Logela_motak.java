package modelo.pojo;

public class Logela_motak {
	
	private String logelaKodea;
	private String logelaDeskribapena;
	
	public Logela_motak(String logelaKodea, String logelaDeskribapena) {
		this.logelaKodea = logelaKodea;
		this.logelaDeskribapena = logelaDeskribapena;
	}

	public String getLogelaKodea() {
		return logelaKodea;
	}

	public void setLogelaKodea(String logelaKodea) {
		this.logelaKodea = logelaKodea;
	}

	public String getLogelaDeskribapena() {
		return logelaDeskribapena;
	}

	public void setLogelaDeskribapena(String logelaDeskribapena) {
		this.logelaDeskribapena = logelaDeskribapena;
	}
	
	
}
