package modelo;

public class Hiria {
	private int kode;
	private String izena;
	
	public Hiria(int kode, String izena) {
		super();
		this.kode = kode;
		this.izena = izena;
	}

	public int getKode() {
		return kode;
	}

	public void setKode(int kode) {
		this.kode = kode;
	}

	public String getIzena() {
		return izena;
	}

	public void setIzena(String izena) {
		this.izena = izena;
	}
	
}
